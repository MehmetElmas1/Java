package secondpackage;

public class ForWhileLoopAbc {
    public static void main(String[] args) {

        for(int i=1; i<6; i++){
            int number=1;
            System.out.println(i);
            while(number<5){
                System.out.print(i+ "\t" +number);
            }
        }
        System.out.println();
    }
}
